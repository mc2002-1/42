#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	result;

	result = 1;
	if (nb < 0)
		return (0);
	while (nb > 1)
	{
		result *= nb;
		nb--;
	}
	return (result);
}

int main()
{
    int nb = 5698230;
    printf("%i", ft_iterative_factorial(nb));

    return 0;
}

